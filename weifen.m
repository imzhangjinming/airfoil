function zhi = weifen(Tt,lij)
%都是三点微分么直接写个通用的好了。返回的是中间的微分部分。边界条件需要另行处理。
%讲道理，这个本来应该是直接写成大小一样的就好了。现在每次都还要手动改改首位，还是有点不爽。
[N25,chang] = size(lij) ; 
% diyixiang = (2*lij(1:N25-2,:) - lij(2:N25-1,:) - lij(3:N25,:))./...
%     ((lij(1:N25-2,:) - lij(3:N25,:)).*(lij(1:N25-2,:)-lij(2:N25-1,:))).*...
%     Tt(1:N25-2,:); 
diyixiang = (lij(2:N25-1,:) - lij(3:N25,:))./...
    ((lij(1:N25-2,:) - lij(3:N25,:)).*(lij(1:N25-2,:)-lij(2:N25-1,:))).*...
    Tt(1:N25-2,:); 
dierxiang = (2*lij(2:N25-1,:) - lij(1:N25-2,:) - lij(3:N25,:))./...
    ((lij(2:N25-1,:) - lij(1:N25-2,:)).*(lij(2:N25-1,:)-lij(3:N25,:))).*...
    Tt(2:N25-1,:); 
% disanxiang = (2*lij(3:N25,:) - lij(1:N25-2,:) - lij(2:N25-1,:))./...
%     ((lij(3:N25,:) - lij(1:N25-2,:)).*(lij(3:N25,:)-lij(2:N25-1,:))).*...
%     Tt(3:N25,:); 
disanxiang = (-lij(1:N25-2,:) + lij(2:N25-1,:))./...
    ((lij(3:N25,:) - lij(1:N25-2,:)).*(lij(3:N25,:)-lij(2:N25-1,:))).*...
    Tt(3:N25,:); 
zhi = diyixiang + dierxiang +disanxiang ; 
end 